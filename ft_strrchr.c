char	*ft_strrchr(const char *s, int c)
{
	char	*res;

	res = (char *)0;
	while (*s)
	{
		if (*s == (char)c)
			res = (char *)s;
		s++;
	}
	return (c == 0 ? (char *)s : res);
}
